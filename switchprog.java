class switchprog
{
	public static void main(String[] args)
	{
		int choice=1;
		switch(choice)
		{
			case 1: System.out.print("Awesome");
							break;
			case 2: System.out.print("Good");
							break;				
			case 3: System.out.print("Average");
							break;
			default: System.out.print("Wrong choice");
		}
	}
}